/**
 * 按 tags 匹配课程
 */
export function getCoursesForTopic(topicTags, count = 3) {
  if (!topicTags || topicTags.length === 0) return [];

  const lowerTags = topicTags.map((t) => t.toLowerCase());

  const scored = mockCourses.map((course) => {
    let matchCount = 0;
    for (const tag of course.tags) {
      if (lowerTags.some((t) => tag.toLowerCase().includes(t) || t.includes(tag.toLowerCase()))) {
        matchCount++;
      }
    }
    return { ...course, matchCount };
  });

  return scored
    .filter((c) => c.matchCount > 0)
    .sort((a, b) => b.matchCount - a.matchCount)
    .slice(0, count);
}
